package swimclub.models;

/**
 * Enum representing the membership category for swimmers.
 * This enum categorizes members based on their swimming focus.
 */
public enum MembershipCategory {

    /**
     * Represents a competitive swimmer who competes in events.
     */
    COMPETITIVE, // Competitive swimmer

    /**
     * Represents a swimmer who participates for exercise or leisure purposes.
     */
    EXERCISE     // Exercise swimmer
}
