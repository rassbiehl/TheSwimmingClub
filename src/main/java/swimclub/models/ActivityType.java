package swimclub.models;

/**
 * Enum representing different types of swimming activities.
 * This enum is used to categorize various swimming strokes.
 */
public enum ActivityType {

    /**
     * Represents the Crawl swimming stroke.
     */
    CRAWL,

    /**
     * Represents the Breaststroke swimming stroke.
     */
    BREASTSTROKE,

    /**
     * Represents the Butterfly swimming stroke.
     */
    BUTTERFLY,

    /**
     * Represents the Backcrawl swimming stroke.
     */
    BACKCRAWL
}
