package swimclub.models;

/**
 * Enum representing the membership level based on age.
 * This enum categorizes members as either Junior or Senior based on their age.
 */
public enum MembershipLevel {

    /**
     * Represents a junior member (under 18 years old).
     */
    JUNIOR, // Junior member (under 18)

    /**
     * Represents a senior member (18 years or older).
     */
    SENIOR  // Senior member (18 and older)
}
